var base = 5;
var pow =5;

//Using Math package and Pow function in it. This is same as in JAVA
var result1 = Math.pow(base,pow);
console.log('The power of base raised to pow is : '+ result1);


//Using ** operator to find exponentation available in JS.  This is NOT available in JAVA
var result2= base**pow;
console.log('The power of base raised to pow is : '+ result2);